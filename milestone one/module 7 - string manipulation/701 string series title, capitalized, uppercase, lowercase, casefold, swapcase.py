sentance_one = 'hello, my name is al amin. i lived in narayangonj'
sentance_two = 'Hello, My Name Is rafiq. I Lived In Narayangonj'

print('title case: - ',sentance_one.title()) #first charecter of each word will be capital
print('capitalize case: - ',sentance_one.capitalize()) # first charecter of the sentance will be capital
print('upper case: - ', sentance_one.upper()) # all the charecter of sentance will be uppercase
print('lowercase: - ', sentance_two.lower())# all the charecter of sentance will be lowercase
# casefold is more powerfull then lowercase.
print('casefold: - ', sentance_two.casefold()) # casefold is similar to lowercase but more powerfull
print('swapcase: - ', sentance_two.swapcase())# uppercase will be lowercase and lowercase will be upercase.